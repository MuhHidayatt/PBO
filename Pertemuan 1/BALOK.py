print("Menghitung luas dan volume balok")

# variable
panjang = 6
lebar = 4
tinggi = 5

# rumus
luas = 2 * ((panjang * lebar) + (panjang * tinggi) + (lebar * tinggi))
volume = panjang * lebar * tinggi

# output
print("Panjang :", panjang)
print("Lebar :", lebar)
print("Tinggi :", tinggi)
print("Luas Balok ", luas)
print("Volume Balok: ", volume)
